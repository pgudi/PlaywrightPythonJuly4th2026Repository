A = {0,2,4,6,3}
B= {1,2,6,7,8,9}

print("Set A :",A)
print("Set B :",B)
# Union Operation  (provides Unique All Elements)
print("A UNION B :", (A | B))
# Intersect Operation (Common Elements)
print("A INTERSECT B :", (A & B))
# Minus Operation  (Elements from Frist Set teh same Which are not afind in second Set)
print("A MINUS B :", (A - B))
# Symmetric Difference  Operation (Inverse of Intersect)
print("A Symmetric Difference B :", (A ^ B))
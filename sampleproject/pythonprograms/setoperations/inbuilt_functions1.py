fruits={10,12.75,True,False,"Mango","Apple","Orange"}
print(fruits)

# How to Add Eleemnts
fruits.add("Water melon")
fruits.add("Jack Fruit")
print(fruits)

# How to Remove Element from set
fruits.remove("Apple")
print(fruits)

# Pop functon to Remove Element
fruits.pop()
print(fruits)
fruits.pop()
print(fruits)

# clear function removes all elements
fruits.clear()
print(fruits)
